    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try {
            String combo1=jComboBox1.getSelectedItem().toString();
        String combo2=jComboBox2.getSelectedItem().toString();
        il.comboBox2Listele(combo1);
//        il.ilceSil(jComboBox2.getSelectedItem().toString()),combo);
        il.ilceSil(combo1,combo2);
        
        String combo=jComboBox1.getSelectedItem().toString();
        jComboBox2.setModel(il.comboBox2Listele(combo));    
        } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Kayıtlarda silinecek ilçe yoktur ...");
        }
        
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

