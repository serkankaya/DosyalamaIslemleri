    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(jTextField1.getText().equals("")){
       JOptionPane.showMessageDialog(null, "Lütfen Kaydedilecek il ismini giriniz ...");
       jTextField1.requestFocus();
       }else{
       degisken.setIl(jTextField1.getText());
       il.dosyaOlustur(degisken.getIl());
       jComboBox1.setModel(il.comboBoxListele());
       il.comboBox2Listele(jComboBox1.getSelectedItem().toString());
       }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        degisken.setIlce(jTextField2.getText());
        degisken.setIl(jTextField1.getText());
        il.dosyayaKaydet(degisken.getIl(),degisken.getIlce());
        
                
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
        String sil=jComboBox1.getSelectedItem().toString();
        il.dosyaSil(sil);
        
        jComboBox1.setModel(il.comboBoxListele());
        il.comboBox2Listele(jComboBox1.getSelectedItem().toString());
        jTextField1.setText(il.comboBoxListele().getSelectedItem().toString());
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Silinecek il bulunamadı ...");
        }
        jTextField1.setText(null);
        

    }//GEN-LAST:event_jButton3ActionPerformed

